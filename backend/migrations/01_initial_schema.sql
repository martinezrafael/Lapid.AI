CREATE TYPE note_status AS ENUM ('PROCESSING', 'PROCESSED', 'FAILED');

CREATE TABLE flows (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL UNIQUE,
  subfolder VARCHAR(255) NOT NULL,
  config_json_name VARCHAR(255) NOT NULL
);

CREATE TABLE notes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  slug VARCHAR(255) NOT NULL UNIQUE,         
  flow_id BIGINT REFERENCES flows(id) ON DELETE RESTRICT,
  raw_content TEXT NOT NULL,
  refined_content TEXT,
  status note_status NOT NULL DEFAULT 'PROCESSING',
  attributes JSONB NOT NULL DEFAULT '{}',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);